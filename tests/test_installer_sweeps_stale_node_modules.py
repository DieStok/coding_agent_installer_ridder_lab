"""HPC-mode install sweeps stale host-side tools/node_modules/.

Pre-9b87b0b installs put biome at <install_dir>/tools/node_modules/.bin/biome.
After 9b87b0b biome lives in the SIF; the host copy is dead weight that
shadows the SIF copy on PATH if not removed. _install_tools must delete
it on every HPC-mode invocation. Local mode is untouched (no SIF, host
biome is the only copy).
"""
from __future__ import annotations

import asyncio
from pathlib import Path
from unittest.mock import MagicMock

import pytest


@pytest.fixture
def fake_install_dir(tmp_path):
    p = tmp_path / "install"
    (p / "bin").mkdir(parents=True)
    return p


def test_install_tools_sweeps_node_modules_in_hpc_mode(fake_install_dir, monkeypatch):
    from coding_agents.installer import executor

    # Pre-populate as if a pre-9b87b0b install had run.
    stale = fake_install_dir / "tools" / "node_modules" / ".bin"
    stale.mkdir(parents=True)
    (stale / "biome").write_text("#!/bin/sh\n")

    log = MagicMock()
    log.write = MagicMock()

    asyncio.run(executor._install_tools([], fake_install_dir, log, mode="hpc"))

    assert not (fake_install_dir / "tools" / "node_modules").exists(), (
        "HPC mode must sweep tools/node_modules/ — left-over host biome "
        "would shadow the SIF copy on PATH."
    )
    log_calls = " ".join(c.args[0] for c in log.write.call_args_list if c.args)
    assert "swept stale tools/node_modules" in log_calls.lower() or "swept stale" in log_calls.lower()


def test_install_tools_preserves_node_modules_in_local_mode(fake_install_dir, monkeypatch):
    """Local mode has no SIF; the host node_modules IS the install. Don't
    touch it."""
    from coding_agents.installer import executor

    stale = fake_install_dir / "tools" / "node_modules" / ".bin"
    stale.mkdir(parents=True)
    biome = stale / "biome"
    biome.write_text("#!/bin/sh\n")

    # Stub the per-tool installs so we only exercise the sweep logic.
    async def _noop(fn, *a, **kw):
        return None
    monkeypatch.setattr(executor, "_run_in_thread", _noop)

    log = MagicMock()
    log.write = MagicMock()

    asyncio.run(executor._install_tools([], fake_install_dir, log, mode="local"))

    assert biome.exists(), "local mode must NOT sweep tools/node_modules/"


def test_install_tools_no_op_when_node_modules_absent_in_hpc(fake_install_dir, monkeypatch):
    """No pre-existing dir → no error and no spurious 'swept' log line."""
    from coding_agents.installer import executor

    log = MagicMock()
    log.write = MagicMock()

    asyncio.run(executor._install_tools([], fake_install_dir, log, mode="hpc"))

    log_calls = " ".join(c.args[0] for c in log.write.call_args_list if c.args)
    assert "swept" not in log_calls.lower()
